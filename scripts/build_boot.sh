#!/usr/bin/env bash
set -euo pipefail

ROOT=$(CDPATH= cd -- "$(dirname -- "$0")/.." && pwd)
OUT="$ROOT/build/gros-v0.3.gro"

mkdir -p "$ROOT/build"
rm -f "$OUT"

# GrBoot v0.3 raw machine bytes.
# This does not compile C, Python, or assembly. It writes machine bytes directly to the file.

printf '%b' '\xfa\x31\xc0\x8e\xd8\x8e\xc0\x8e\xd0\xbc\x00\x7c\xfb\xbe\x9e\x7c\xe8\x73\x00\xbe\xaa\x7c\xe8\x6d\x00\xbf\xda\x7c\x31\xc9\x31\xc0\xcd\x16\x3c\x0d\x74\x24\x3c\x08\x74\x11\x3c\x20\x72\xf0\x83\xf9\x0f\x73\xeb\xaa\x41\xb4\x0e\xcd\x10\xeb\xe3\x83\xf9\x00\x74\xde\x4f\x49\xbe\xb2\x7c\xe8\x3e\x00\xeb\xd4\xb0\x00\xaa\xbe\xaf\x7c\xe8\x33\x00\x83\xf9\x00\x74\xbb\xbe\xda\x7c\xbf\xb6\x7c\xe8\x31\x00\x72\x13\xbe\xda\x7c\xbf\xbb\x7c\xe8\x26\x00\x72\x10\xbe\xd6\x7c\xe8\x12\x00\xeb\x9d\xbe\xbf\x7c\xe8\x0a\x00\xeb\x95\xbe\xca\x7c\xe8\x02\x00\xeb\x8d\xac\x84\xc0\x74\x06\xb4\x0e\xcd\x10\xeb\xf5\xc3\xac\xae\x75\x06\x84\xc0\x75\xf8\xf9\xc3\xf8\xc3\x47\x72\x4f\x53\x20\x76\x30\x2e\x33\x0d\x0a\x00\x67\x72\x3e\x20\x00\x0d\x0a\x00\x08\x20\x08\x00\x68\x65\x6c\x70\x00\x76\x65\x72\x00\x68\x65\x6c\x70\x20\x76\x65\x72\x0d\x0a\x00\x47\x72\x4f\x53\x20\x76\x30\x2e\x33\x0d\x0a\x00\x3f\x0d\x0a\x00' > "$OUT"

SIZE=$(wc -c < "$OUT" | tr -d ' ')
PAD=$((510 - SIZE))

if [ "$PAD" -lt 0 ]; then
    echo "error: boot sector is too large: $SIZE bytes"
    exit 1
fi

dd if=/dev/zero bs=1 count="$PAD" >> "$OUT" 2>/dev/null
printf $'\x55\xaa' >> "$OUT"

FINAL_SIZE=$(wc -c < "$OUT" | tr -d ' ')
echo "built: $OUT"
echo "size : $FINAL_SIZE bytes"
